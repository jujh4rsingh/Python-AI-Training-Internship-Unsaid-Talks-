# This program adds two numbers provided by the user

# Get input from the user
num1 = input("Enter the first number: ")
num2 = input("Enter the second number: ")

# Convert the input from string to float
num1 = float(num1)
num2 = float(num2)

# Add the two numbers
sum = num1 + num2

# Display the result
print("The sum is:", sum)
